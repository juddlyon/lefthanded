#!/usr/bin/env node

/**
 * Generates public/_redirects with /go/{slug} → Amazon affiliate redirects
 * from src/data/products.json.
 *
 * Pattern (mirrors ~/projects/paintballer, ~/projects/modernpb.com):
 *   /go/{slug}  https://www.amazon.com/dp/{ASIN}?tag={TAG}  302   (when ASIN known)
 *   /go/{slug}  https://www.amazon.com/s?k={query}&tag={TAG}  302  (search fallback)
 *
 * Why cloak:
 *  - clean URLs without affiliate tag exposed in HTML
 *  - one place to update tag if it changes
 *  - one place to swap retailer (Amazon → another) without touching content
 *  - centralized broken-ASIN audit
 *
 * Run automatically via npm prebuild — see package.json.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const PRODUCTS_PATH = path.join(ROOT, 'src', 'data', 'products.json');
const REDIRECTS_PATH = path.join(ROOT, 'public', '_redirects');

const ASSOCIATE_TAG = 'lefthanded-io-20';
const MARKER_START = '# --- Amazon Affiliate Redirects (auto-generated by scripts/generate-redirects.mjs) ---';
const MARKER_END = '# --- End Amazon Affiliate Redirects ---';

function buildRedirects(products) {
  const lines = [];
  const slugs = Object.keys(products).sort();

  for (const slug of slugs) {
    const entry = products[slug] || {};
    const asin = (entry.asin || '').trim();

    if (asin) {
      lines.push(`/go/${slug}  https://www.amazon.com/dp/${asin}?tag=${ASSOCIATE_TAG}  302`);
      continue;
    }

    const queryText = (entry.query || entry.name || slug.replace(/-/g, ' ')).trim();
    const q = encodeURIComponent(queryText);
    lines.push(`/go/${slug}  https://www.amazon.com/s?k=${q}&tag=${ASSOCIATE_TAG}  302`);
  }

  return lines;
}

function preserveExisting(text) {
  // Strip the auto-generated block; keep manual redirects, comments, etc.
  if (!text) return '';
  const startIdx = text.indexOf(MARKER_START);
  const endIdx = text.indexOf(MARKER_END);
  if (startIdx === -1 || endIdx === -1) return text.trim();
  const before = text.slice(0, startIdx).trimEnd();
  const after = text.slice(endIdx + MARKER_END.length).trimStart();
  return [before, after].filter(Boolean).join('\n').trim();
}

function main() {
  if (!fs.existsSync(PRODUCTS_PATH)) {
    console.warn(`! products.json missing at ${PRODUCTS_PATH} — skipping redirect generation`);
    return;
  }

  const products = JSON.parse(fs.readFileSync(PRODUCTS_PATH, 'utf-8'));
  const lines = buildRedirects(products);
  const block = [MARKER_START, ...lines, MARKER_END].join('\n');

  let existing = '';
  if (fs.existsSync(REDIRECTS_PATH)) {
    existing = preserveExisting(fs.readFileSync(REDIRECTS_PATH, 'utf-8'));
  }

  const final = existing ? `${existing}\n\n${block}\n` : `${block}\n`;
  fs.mkdirSync(path.dirname(REDIRECTS_PATH), { recursive: true });
  fs.writeFileSync(REDIRECTS_PATH, final);

  const direct = lines.filter((l) => l.includes('/dp/')).length;
  const search = lines.filter((l) => l.includes('/s?k=')).length;
  console.log(`✓ Wrote ${lines.length} affiliate redirects to ${path.relative(ROOT, REDIRECTS_PATH)}`);
  console.log(`  ${direct} direct ASIN, ${search} search fallback`);
}

main();
